package com.revature.models;

public class AccountStatus {
	private int id; // primary key
	private String status; // not null, unique
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
